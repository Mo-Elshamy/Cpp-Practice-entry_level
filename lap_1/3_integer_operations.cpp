#include <iostream>

int main(){
    int x=50 , y=10;

    std::cout<<"X="<<x<<" & Y="<<y<<"\n"; //Print the values 
    std::cout<<"X+Y="<<x+y<<"\n"; // Addition
    std::cout<<"X-Y="<<x-y<<"\n"; // Substraction
    std::cout<<"X*Y="<<x*y<<"\n"; // Multiplication
    std::cout<<"X/Y="<<x/y<<"\n"; // Division
    
    return 0;
}